		<p><h1>Idea generation</h1></p>
		<b>Where do the ideas come from?</b>
	<p class="text-justify">The roles and contributions of the community in innovation contests are well researched. Recent literature [1] differentiates between six different community member types, namely the socializer, the idea generator, the master, the efficient contributor, the passive commentator, and the passive idea generator. The foundation of the typical social structure of online communities is built on voluntary participation, consisting of a large percentage of passive users and a small core of highly active users [2,3]. A description of the characteristicts of the member types can be found in the boxes below:  </p>
<br />


<div class="card-deck">
<div class="card">
	<div class="card-body">
		<h5 class="card-title"><b>Socializer</b></h5>
		<p class="card-text text-justify">
			Socializers are characterized by active communication and interaction behavior with ideators and their designs or ideas, and represent 2.13% of the community participants. Very few socializers submit their own ideas, as they prefer to contribute to the contest community with interaction behavior rather than design ideas.
		</p>
	</div>
</div>

  <div class="card">
    <div class="card-body">
      <h5 class="card-title"><b>Idea generator</b></h5>
      <p class="card-text text-justify">
				Idea generators do not actively engage in communication, facilitate information exchange, or build social relationships. They represent 2.40% ofthe community participants and are these participants that generate most designs or ideas. Their submissions do not necessarily lead to high attention and curiosity by other community members.
			</p>
    </div>
  </div>

  <div class="card">
    <div class="card-body">
      <h5 class="card-title"><b>Master</b></h5>
      <p class="card-text text-justify">
				Just 1% of the participants in the innovation contest community are identified as Masters. These participants are both active community members and submit several ideas. The submissions of Masters attract a high level of attention within the contest community. Masters are valuable community participants due to the high quality of their submitted ideas and their active participation.
			</p>
    </div>
  </div>
</div class="card-deck">
<br />

<div class="card-deck">
	<div class="card">
		<div class="card-body">
			<h5 class="card-title"><b>Efficient contributor</b></h5>
			<p class="card-text text-justify">
				The Efficient Contributors, representing 8.4% of the community population,  are similar to Idea Generators, but with a different level of productivity. These participants focus on idea-generating activities instead of active interaction. The Efficient Contributors submit substantially less ideas than the Idea Generators, but attract considerably more attention due to the constructive feedback and the suggestions for improvement they provide.
			</p>
		</div>
	</div>
	<div class="card">
		<div class="card-body">
			<h5 class="card-title"><b>Passive users</b></h5>
			<p class="card-text text-justify">
				The majority of the participants in the contest community are characterised as passive users, divided into passive commentators (47%) and passive idea generators (39%).
				Passive users neither generate ideas nor engage in communication or interaction with other community members. While passive commentators provide very few comments, passive idea generators submit on average one idea but do not contribute further.
			</p>
		</div>
